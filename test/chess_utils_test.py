        # Test round-trip conversion
        fen = 'rnbqkbnr/pppppppp/8/8/8/8/PPPPPPPP/RNBQKBNR w KQkq - 0 1'
        feature_vector = fen_to_vec(fen)
        converted_fen = vec_to_fen(feature_vector)
        self.assertEqual(fen, converted_fen)

